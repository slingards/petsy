const searchInput =
document.getElementById(
    "catSearch"
);

const breedFilter =
document.getElementById(
    "catFilter"
);

function filterCats(){

    const searchTerm =
    searchInput ?
    searchInput.value.toLowerCase()
    : "";

    const category =
    breedFilter ?
    breedFilter.value
    : "all";

    const filteredCats =
    cats.filter(cat=>{

        const nameMatch =

        cat.name
        .toLowerCase()
        .includes(searchTerm);

        const categoryMatch =

        category === "all" ||

        cat.category === category;

        return (

            nameMatch &&
            categoryMatch

        );

    });

    renderCats(
        filteredCats
    );

    const results =
    document.getElementById(
        "resultsCount"
    );

    if(results){

        results.textContent =

        `${filteredCats.length} Cats Available`;

    }

}

if(searchInput){

    searchInput.addEventListener(
        "input",
        filterCats
    );

}

if(breedFilter){

    breedFilter.addEventListener(
        "change",
        filterCats
    );

}

function attachButtons(){

    document
    .querySelectorAll(
        ".add-cart-btn"
    )
    .forEach(button=>{

        button.onclick = ()=>{

            addToCart({

                name:
                button.dataset.name,

                price:
                button.dataset.price,

                image:
                button.dataset.image

            });

        };

    });

}

function animateCards(){

    const cards =
    document.querySelectorAll(
        ".pet-card"
    );

    const observer =
    new IntersectionObserver(

        entries => {

            entries.forEach(
                entry => {

                    if(
                        entry.isIntersecting
                    ){

                        entry.target
                        .classList.add(
                            "show"
                        );

                    }

                }
            );

        },

        {
            threshold:0.15
        }

    );

    cards.forEach(card=>{

        observer.observe(card);

    });

}

