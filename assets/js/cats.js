const catsGrid =
document.getElementById(
    "catsGrid"
);

function renderCats(catsArray){

    if(!catsGrid) return;

    catsGrid.innerHTML = "";

    catsArray.forEach(cat=>{

        catsGrid.innerHTML += `

        <div
        class="pet-card"
        data-category="${cat.category}"
        data-name="${cat.name.toLowerCase()}">

            <img
            src="../${cat.image}"
            alt="${cat.name}">

            <div class="pet-content">

                <span class="pet-category">

                    Cat

                </span>

                <h3>

                    ${cat.name}

                </h3>

                <p>

                    ${cat.description}

                </p>

                <div class="pet-info">

                    <span>

                        ${cat.age}

                    </span>

                    <span>

                        ${cat.location}

                    </span>

                </div>

                <div class="pet-footer">

                    <h4>

                        $${cat.price}

                    </h4>

                    <div class="pet-actions">

                        <button
                        class="primary-btn add-cart-btn"
                        data-name="${cat.name}"
                        data-price="${cat.price}"
                        data-image="../${cat.image}">

                            Add To Cart

                        </button>

                    </div>

                </div>

            </div>

        </div>

        `;

    });

    attachButtons();

    animateCards();

}

const searchInput =
document.getElementById(
    "catSearch"
);

const breedFilter =
document.getElementById(
    "catFilter"
);

function filterCats(){

    const searchTerm =
    searchInput ?
    searchInput.value.toLowerCase()
    : "";

    const category =
    breedFilter ?
    breedFilter.value
    : "all";

    const filteredCats =
    cats.filter(cat=>{

        const nameMatch =

        cat.name
        .toLowerCase()
        .includes(searchTerm);

        const categoryMatch =

        category === "all" ||

        cat.category === category;

        return (

            nameMatch &&
            categoryMatch

        );

    });

    renderCats(
        filteredCats
    );

    const results =
    document.getElementById(
        "resultsCount"
    );

    if(results){

        results.textContent =

        `${filteredCats.length} Cats Available`;

    }

}

if(searchInput){

    searchInput.addEventListener(
        "input",
        filterCats
    );

}

if(breedFilter){

    breedFilter.addEventListener(
        "change",
        filterCats
    );

}

function attachButtons(){

    document
    .querySelectorAll(
        ".add-cart-btn"
    )
    .forEach(button=>{

        button.onclick = ()=>{

            addToCart({

                name:
                button.dataset.name,

                price:
                button.dataset.price,

                image:
                button.dataset.image

            });

        };

    });

}

function animateCards(){

    const cards =
    document.querySelectorAll(
        ".pet-card"
    );

    const observer =
    new IntersectionObserver(

        entries => {

            entries.forEach(
                entry => {

                    if(
                        entry.isIntersecting
                    ){

                        entry.target
                        .classList.add(
                            "show"
                        );

                    }

                }
            );

        },

        {
            threshold:0.15
        }

    );

    cards.forEach(card=>{

        observer.observe(card);

    });

}

