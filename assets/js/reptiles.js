function normalizeAnimalWishlistImagePath(path){

    let cleanPath =
    String(path || "")
    .trim();

    const assetIndex =
    cleanPath.indexOf(
        "assets/images/"
    );

    if(assetIndex !== -1){

        cleanPath =
        cleanPath.substring(
            assetIndex
        );

    }

    cleanPath =
    cleanPath
    .replace(/^\.\.\//g, "")
    .replace(/^\.\//g, "")
    .replace(/^\//g, "");

    return cleanPath;

}

function getAnimalWishlist(){

    return JSON.parse(
        localStorage.getItem(
            "CompanionReviewHubWishlist"
        )
    ) || [];

}

function saveAnimalWishlist(list){

    localStorage.setItem(
        "CompanionReviewHubWishlist",
        JSON.stringify(list)
    );

    updateAnimalWishlistCount();

}

function updateAnimalWishlistCount(){

    const count =
    document.getElementById(
        "wishlist-count"
    );

    if(!count) return;

    const list =
    getAnimalWishlist();

    count.textContent =
    list.length;

}

