function animateCards(){

    const cards =
    document.querySelectorAll(
        ".pet-card"
    );

    const observer =
    new IntersectionObserver(

        (entries)=>{

            entries.forEach(
                entry=>{

                    if(
                        entry.isIntersecting
                    ){

                        entry.target.classList.add(
                            "show"
                        );

                        observer.unobserve(
                            entry.target
                        );

                    }

                }
            );

        },

        {
            threshold:0.25,
            rootMargin:"0px 0px -50px 0px"
        }

    );

    cards.forEach(card=>{

        card.classList.remove(
            "show"
        );

        observer.observe(
            card
        );

    });

}

