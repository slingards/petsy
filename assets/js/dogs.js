function filterDogs(){

    const searchTerm =
    searchInput ?
    searchInput.value.toLowerCase()
    : "";

    const category =
    breedFilter ?
    breedFilter.value
    : "all";

    const filteredDogs =
    dogs.filter(dog=>{

        const nameMatch =

        dog.name
        .toLowerCase()
        .includes(searchTerm);

        const categoryMatch =

        category === "all" ||

        dog.category === category;

        return (

            nameMatch &&
            categoryMatch

        );

    });

    renderDogs(
        filteredDogs
    );

    document.getElementById(
        "resultsCount"
    ).textContent =

    `${filteredDogs.length} Dog Profiles`;

}


