const dogsGrid =
document.getElementById(
    "dogsGrid"
);

function renderDogs(dogsArray){

    dogsGrid.innerHTML = "";

    dogsArray.forEach(dog=>{

        dogsGrid.innerHTML += `

        <div

        class="pet-card"

        data-category="${dog.category}"

        data-name="${dog.name.toLowerCase()}">

            <img
            src="../${dog.image}"
            alt="${dog.name}">

            <div class="pet-content">

                <span class="pet-category">

                    Dog

                </span>

                <h3>

                    ${dog.name}

                </h3>

                <p>

                    ${dog.description}

                </p>

                <div class="pet-info">

                    <span>

                        ${dog.age}

                    </span>

                    <span>

                        ${dog.location}

                    </span>

                </div>

                <div class="pet-footer">

                    <h4>

                        $${dog.price}

                    </h4>

                    <div class="pet-actions">

                        <div class="pet-actions">

                            <button

                            class="primary-btn add-cart-btn"

                            data-name="${dog.name}"

                            data-price="${dog.price}"

                            data-image="../${dog.image}">

                                Add To Cart

                            </button>
                        </div>

                    </div>

                </div>

            </div>

        </div>

        `;

    });

    attachButtons();

    animateCards();

}

const searchInput =
document.getElementById(
    "dogSearch"
);

const breedFilter =
document.getElementById(
    "breedFilter"
);

function filterDogs(){

    const searchTerm =
    searchInput.value
    .toLowerCase();

    const category =
    breedFilter.value;

    const filteredDogs =
    dogs.filter(dog=>{

        const nameMatch =

        dog.name
        .toLowerCase()
        .includes(searchTerm);

        const categoryMatch =

        category === "all" ||

        dog.category === category;

        return (

            nameMatch &&
            categoryMatch

        );

    });

    renderDogs(
        filteredDogs
    );

    document.getElementById(
        "resultsCount"
    ).textContent =

    `${filteredDogs.length} Breeds Available`;

}


searchInput.addEventListener(
    "input",
    filterDogs
);

breedFilter.addEventListener(
    "change",
    filterDogs
);


renderDogs(dogs);


function attachButtons(){

    document
    .querySelectorAll(
        ".add-cart-btn"
    )
    .forEach(button=>{

        button.onclick = ()=>{

            addToCart({

                name:
                button.dataset.name,

                price:
                button.dataset.price,

                image:
                button.dataset.image

            });

        };

    });

}


function animateCards(){

    const cards =
    document.querySelectorAll(
        ".pet-card"
    );

    const observer =
    new IntersectionObserver(

        (entries)=>{

            entries.forEach(
                entry=>{

                    if(
                        entry.isIntersecting
                    ){

                        entry.target.classList.add(
                            "show"
                        );

                        observer.unobserve(
                            entry.target
                        );

                    }

                }
            );

        },

        {
            threshold:0.25,
            rootMargin:"0px 0px -50px 0px"
        }

    );

    cards.forEach(card=>{

        card.classList.remove(
            "show"
        );

        observer.observe(
            card
        );

    });

}

const sections =
document.querySelectorAll(
    ".reveal"
);

const revealObserver =
new IntersectionObserver(

    entries=>{

        entries.forEach(
            entry=>{

                if(
                    entry.isIntersecting
                ){

                    entry.target
                    .classList.add(
                        "active"
                    );

                }

            }
        );

    },

    {
        threshold:0.1
    }

);

sections.forEach(
    section=>{

        revealObserver.observe(
            section
        );

    }
);