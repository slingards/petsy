function normalizeAnimalWishlistImagePath(path){

    let cleanPath =
    String(path || "")
    .trim();

    const assetIndex =
    cleanPath.indexOf(
        "assets/images/"
    );

    if(assetIndex !== -1){

        cleanPath =
        cleanPath.substring(
            assetIndex
        );

    }

    cleanPath =
    cleanPath
    .replace(/^\.\.\//g, "")
    .replace(/^\.\//g, "")
    .replace(/^\//g, "");

    return cleanPath;

}

